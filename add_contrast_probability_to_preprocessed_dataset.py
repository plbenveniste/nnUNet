"""
This script adds the contrast probability to the dataset.json file of the nnUNet preprocessed dataset.

Input: 
    - conversion_dict.json: A JSON file that contains the mapping of original file names to their corresponding preprocessed file names.
    - dataset.json: A JSON file that contains the metadata of the preprocessed dataset. (This file is generated by nnUNet during preprocessing and stored in the nnUNet_preprocessed dataset folder.)

Output:
    None

Example:
    python add_contrast_probability_to_preprocessed_dataset.py -c /path/to/conversion_dict.json -d /path/to/dataset.json

Author: Pierre-Louis Benveniste
"""
import json
import argparse


def parse_args():
    parser = argparse.ArgumentParser(description="Add contrast probability to dataset.json")
    parser.add_argument("-c", "--conversion_dict", type=str, required=True, help="Path to the conversion dictionary JSON file")
    parser.add_argument("-d", "--dataset_json", type=str, required=True, help="Path to the dataset.json file")
    args = parser.parse_args()
    return args


def main():
    args = parse_args()
    path_conversion_dict = args.conversion_dict
    path_preprocessed_dataset_json = args.dataset_json

    print("Don't forget to edit the contrast_ratio dictionary in the code before running it.")

    # Load the conversion dictionary
    conversion_dict = json.load(open(path_conversion_dict, "r"))
    # Load the dataset.json file
    dataset_json = json.load(open(path_preprocessed_dataset_json, "r"))

    # In conversion_dict, we have the file names, remove those with derivatives in name:
    files = [file for file in conversion_dict.keys() if "derivatives" not in file]

    # Contrast ratio: equal to 1/sqrt(Number of images in the contrast)
    contrast_ratio = {
        "UNIT1": 0.054,
        "STIR": 0.104,
        "PSIR": 0.052,
        "T2star": 0.043,
        "MEGRE": 0.043,
        "T1w": 0.209,
        "T2w": 0.018,
    }

    # Add probability to each relevant entry
    for file in files:
        contrast = file.split("_")[-1].split(".")[0]
        corresponding_file = conversion_dict[file].replace("_0000", "")
        folder = "training" if "imagesTr" in corresponding_file else "test"
        for entry in dataset_json[folder]:
            if entry["image"] == corresponding_file:
                entry["probability"] = contrast_ratio.get(contrast, 0.0)
                # print(f"Added probability {contrast_ratio[contrast]} to file {entry['image']} with contrast {contrast}")
                break  # Stop once match is found

    # Save the modified dataset.json
    with open(path_preprocessed_dataset_json, "w") as f:
        json.dump(dataset_json, f, indent=4)
    print("Added probability to dataset.json file.")


if __name__ == "__main__":
    main()